public class WebCam {
    private String placeName;
    private String internalName;
    private String externalUrl;
    private String cameraType;
    private String latitude;
    private String longitude;
    private String elevation;
	private boolean weatherStation;
	private String escenicUrl;
	private Date lastUpdated;

	public Date getLastUpdated() {
        return lastUpdated;
    }

    public void setLastUpdated(Date lastUpdated) {
        this.lastUpdated = lastUpdated;
    }

	public String getEscenicUrl() {
        return escenicUrl;
    }

    public void setEscenicUrl(String escenicUrl) {
        this.escenicUrl = escenicUrl;
    }

	public boolean getWeatherStation() {
		return weatherStation;
	}

	public void setWeatherStation(boolean ws)
	{
		this.weatherStation = ws;
	}

    public String getInternalName() {
        return internalName;
    }

    public void setInternalName(String internalName) {
        this.internalName = internalName;
    }

    public String getCameraType() {
        return cameraType;
    }

    public void setCameraType(String cameraType) {
        this.cameraType = cameraType;
    }

    public String getExternalUrl() {
        return externalUrl;
    }

    public void setExternalUrl(String externalUrl) {
        this.externalUrl = externalUrl;
    }

    public String getPlaceName() {
        return placeName;
    }

    public void setPlaceName(String placeName) {
        this.placeName = placeName;
    }

    public String getElevation() {
        return elevation;
    }

    public void setElevation(String elevation) {
        this.elevation = elevation;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }
}

